// 文字単位で簡体字→日本語常用漢字に置換した文字列を返す純関数。
// 数値・時刻には漢字が含まれないため、これらの列に通しても無害。
function normalizeKanji(s) {
  if (s == null) return s;
  let out = '';
  for (const ch of String(s)) {
    out += SIMPLIFIED_TO_JP[ch] || ch;
  }
  return out;
}

